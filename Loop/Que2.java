import java.util.*;
 public class Que2{
    //print number from 1 to n
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int range=sc.nextInt();
        int counter=1;
        while(counter<=range){
            System.out.println(counter +" ");
            counter++;
        }
         System.out.println();
        
    }
 }
