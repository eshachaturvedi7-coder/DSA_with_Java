//ARITHMETIC OPERATOR
//arithmetic has two types --> 1- binary , 2- unary 
//so we perform binary operator
public class binary {
    public static void main(String[] args) {
        int A = 15;
        int B = 5;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println("add =" +(A+B));
        System.out.println("modulo(remainder)=" +(A%B));
    }
    
}
